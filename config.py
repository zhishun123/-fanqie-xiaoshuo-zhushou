import re

# 文件名解析正则：匹配 "第XX章_标题.txt" 格式
# 从文件名提取章节号和标题
FILENAME_PATTERN = re.compile(
    r'^第(\d+)章[_\s](.+)\.txt$',
    re.IGNORECASE
)

# 文件内容第一行的章节标题正则（用于提取正文时跳过标题行）
# 匹配 "第X章 标题" 
CONTENT_TITLE_PATTERN = re.compile(
    r'^第[0-9一二三四五六七八九十百千万零〇]+章\s+.+$'
)

# 起始章节号（从第几章开始上传，用于断点续传）
